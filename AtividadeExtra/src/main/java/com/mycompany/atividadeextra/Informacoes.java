    ArrayList<funcionario> funcionario = new ArrayList<>();
    
    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        // TODO add your handling code here:
        
        String cargo = (String) Cargo.getSelectedItem();
        
        if(cargo == "Engenheiro"){

        funcionario c1 = new engenheiro(Nome.getText(),cargo);
        c1.salario(Double.parseDouble(Salario.getText()));
        funcionario.add(c1);
        Area.setText("");
        
        for(funcionario c2 : funcionario){
            Area.setText(Area.getText()+"Nome: "+c2.getNome()+"    Cargo: "+c2.Cargo()+"    Salario: "+c2.getSalario()+"\n");
        }
        }
        
        if(cargo == "Diretor"){

        funcionario c1 = new diretor(Nome.getText(),cargo);
        c1.salario(Double.parseDouble(Salario.getText()));
        funcionario.add(c1);
        Area.setText("");
        
        for(funcionario c2 : funcionario){
            Area.setText(Area.getText()+"Nome: "+c2.getNome()+"    Cargo: "+c2.Cargo()+"    Salario: "+c2.getSalario()+"\n");
        }
        }
        
        if(cargo == "Secretario"){

        funcionario c1 = new secretario(Nome.getText(),cargo);
        c1.salario(Double.parseDouble(Salario.getText()));
        funcionario.add(c1);
        Area.setText("");
        
        for(funcionario c2 : funcionario){
            Area.setText(Area.getText()+"Nome: "+c2.getNome()+"    Cargo: "+c2.Cargo()+"    Salario: "+c2.getSalario()+"\n");
        }
        }
        
        if(cargo == "Gerente"){

        funcionario c1 = new gerente(Nome.getText(),cargo);
        c1.salario(Double.parseDouble(Salario.getText()));
        funcionario.add(c1);
        Area.setText("");
        
        for(funcionario c2 : funcionario){
            Area.setText(Area.getText()+"Nome: "+c2.getNome()+"    Cargo: "+c2.Cargo()+"    Salario: "+c2.getSalario()+"\n");
        }
        }
        
        
    }//GEN-LAST:event_RegistrarActionPerformed

